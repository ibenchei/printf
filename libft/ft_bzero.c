#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*tmp;

	i = 0;
	tmp = s;
	if (!s)
		return ;
	while (i < n)
	{
		tmp[i] = '\0';
		i++;
	}
}
